package la;

public class Const
{
	private String cons;
	private int num;
	
	public Const(String cons,int num)
	{
		this.cons=cons;
		this.num=num;
	}
	public String getCons() {
		return cons;
	}
	public void setCons(String cons) {
		this.cons =cons;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	
}
